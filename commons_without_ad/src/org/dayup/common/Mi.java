package org.dayup.common;

public class Mi {
	private long lc = 0;
	private int v = 0;
	private boolean irk = true;
	private String k1;
	private String k2;
	
	public long getLc() {
		return lc;
	}
	public void setLc(long l) {
		this.lc = l;
	}
	public int getV() {
		return v;
	}
	public void setV(int v) {
		this.v = v;
	}
	public boolean isIrk() {
		return irk;
	}
	public void setIrk(boolean irk) {
		this.irk = irk;
	}
	public String getK1() {
		return k1;
	}
	public void setK1(String k1) {
		this.k1 = k1;
	}
	public String getK2() {
		return k2;
	}
	public void setK2(String k2) {
		this.k2 = k2;
	}
}
